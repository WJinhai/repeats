function [] = draw2d_lafs(ax1,lafs_2d,color)
axes(ax1);

hold on;
x = reshape(lafs_2d(1:2:end,:),3,[]);
y = reshape(lafs_2d(2:2:end,:),3,[]);
plot(x,y); 

hp = scatter(lafs_2d(1:2:end), ...
             lafs_2d(2:2:end));

hp2 = scatter(lafs_2d(1:6:end), ...
              lafs_2d(2:6:end));
hold off;

set(get(hp,'Children'), ... 
    'LineWidth', 1,'Marker', 'o', ...
    'MarkerSize', 4,  ...
    'MarkerFaceColor' , [.75 .75 1]);

set(get(hp2,'Children'), ... 
    'LineWidth', 1,'Marker', 'o', ...
    'MarkerSize', 4,  ...
    'MarkerFaceColor' , [0.1 0.1 0.2]);

